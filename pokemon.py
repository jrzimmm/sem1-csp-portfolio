    time.sleep(0.5)
    print("Your Pokemon needs " + str(xpRequirement - pokemonXP) + " XP to level up to Level " + str(pokemonLevel))
    time.sleep(0.5)
    print("Your Pokemon has a " + str(wins) + "-" + str(losses) + " record. Your Pokemon is on a " + str(winstreak) + " game winstreak")
